IF EXISTS (SELECT * FROM SYS.objects WHERE OBJECT_ID = OBJECT_ID(N'SP_PROCURAR_CARRO'))
	DROP PROCEDURE SP_PROCURAR_CARRO
GO
CREATE PROCEDURE SP_PROCURAR_CARRO
	@MARCA		VARCHAR(100) = NULL,
	@MODELO		VARCHAR(100) = NULL,
	@COR		VARCHAR(100) = NULL,
	@ANO		DATE		 = NULL,
	@PRECO		VARCHAR(100) = NULL,
	@DESCRICAO  VARCHAR(100) = NULL
AS
BEGIN 
	
	SELECT  CAR_MARCA 'MARCA',
			CAR_MODELO 'MODELO',
			CAR_COR 'COR',
			CAR_ANO 'ANO',
			CAR_PRECO 'PREÇO',

			OPC_DESCRICAO 'ACESSORIOS',
	
			FK_CAR_CODIGO,
			FK_OPC_CODIGO

		FROM tb_CARRO CAR
			INNER JOIN tb_OPCIONAL_CARRO OC
				ON CAR.CAR_CODIGO = OC.FK_CAR_CODIGO
			INNER JOIN tb_OPCIONAL OPC
				ON OC.FK_OPC_CODIGO = OPC.OPC_CODIGO
		
			WHERE 
						(@MARCA IS NULL OR (CAR.CAR_MARCA LIKE '%' + @MARCA + '%')) 
					AND (@MODELO IS NULL OR (CAR.CAR_MODELO = '%' + @MODELO + '%'))
					AND (@COR IS NULL OR (CAR.CAR_COR = '%' + @COR + '%'))
					AND (@ANO IS NULL OR (CAR.CAR_ANO = '%' + @ANO + '%'))
					AND (@PRECO IS NULL OR (CAR.CAR_PRECO = '%' + @PRECO + '%'))
					AND (@DESCRICAO IS NULL OR (OPC.OPC_DESCRICAO = '%' + @DESCRICAO + '%'))
			
				  --CAR.CAR_MARCA LIKE '%' + @BUSCAR + '%' OR 
				  --CAR.CAR_MODELO LIKE '%' + @BUSCAR + '%' OR 
				  --CAR.CAR_COR LIKE '%' + @BUSCAR + '%' OR
				  --CAR.CAR_ANO LIKE '%' + @BUSCAR + '%' OR
				  --CAR.CAR_PRECO LIKE '%' + @BUSCAR + '%' OR
				  --OPC_DESCRICAO LIKE '%' + @BUSCAR + '%'

END

EXEC SP_PROCURAR_CARRO '2016'












