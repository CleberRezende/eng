-- CRIAR
IF EXISTS (SELECT * FROM SYS.objects WHERE OBJECT_ID = OBJECT_ID(N'SP_CRIAR_CARRO'))
	DROP PROCEDURE SP_CRIAR_CARRO
GO
CREATE PROCEDURE SP_CRIAR_CARRO
	@MARCA		VARCHAR(50),		
	@MODELO		VARCHAR(50),	
	@COR		VARCHAR(50),		
	@ANO		DATE,		
	@PRECO		MONEY
AS
BEGIN
	INSERT tb_CARRO (
						 CAR_MARCA,	
						 CAR_MODELO,	
						 CAR_COR,		
						 CAR_ANO,		
						 CAR_PRECO	
					 )
	VALUES	(
				@MARCA,	
				@MODELO,	
				@COR,		
				@ANO,		
				@PRECO							
			)
	
	RETURN SCOPE_IDENTITY() 
END






















-- EDITAR
IF EXISTS (SELECT * FROM SYS.objects WHERE OBJECT_ID = OBJECT_ID(N'SP_EDITAR_CARRO'))
	DROP PROCEDURE SP_EDITAR_CARRO
GO
CREATE PROCEDURE SP_EDITAR_CARRO
	@ID			INT,
	@MARCA		VARCHAR(50),		
	@MODELO		VARCHAR(50),	
	@COR		VARCHAR(50),		
	@ANO		DATE,		
	@PRECO		MONEY
AS
BEGIN
	UPDATE tb_CARRO SET 
						CAR_MARCA = @MARCA,	
						CAR_MODELO = @MODELO,	
						CAR_COR = @COR,		
						CAR_ANO = @ANO,		
						CAR_PRECO = @PRECO	
							
	WHERE CAR_CODIGO = @ID
						
END		
EXEC SP_EDITAR_CARRO 4, 'Volkswagen', 'Gol G5', 'Branco', '2016', '23500'















