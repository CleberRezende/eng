USE db_CARRO


RETURN SCOPE_IDENTITY()



-- CRIAR FINANCEIRO
IF EXISTS (SELECT * FROM SYS.objects WHERE OBJECT_ID = OBJECT_ID(N'SP_CRIAR_FINANCEIRO'))
	DROP PROCEDURE SP_CRIAR_FINANCEIRO
GO
CREATE PROCEDURE SP_CRIAR_FINANCEIRO
	
	@VALOR		MONEY,
	@PARCELAS	INT,
	@PERCENTUAL	INT,
	@VENCIMENTO	INT,
	@ID			INT

AS
BEGIN

	INSERT tb_FINANCEIRO	(
								FIN_VALOR,	
								FIN_PARCELAS,
								FIN_PERCENTUAL,	
								FIN_VENCIMENTO,	
								FK_CLI_CODIGO	
							)
	VALUES	(
				@VALOR,		
				@PARCELAS,	
				@PERCENTUAL,	
				@VENCIMENTO,
				@ID
			)

END


